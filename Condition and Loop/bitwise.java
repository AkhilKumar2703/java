public class bitwise {
    public static void main(String[] args) {
        // or bitwise
        System.out.println(2 | 3);
        // and bitwise
        System.out.println(2 & 3);
        // exclusive bitwise
        System.out.println(2 ^ 3);
        // not of bitwise
        System.out.println(~2);
        // left shift
        System.out.println(34 << 1);
        System.out.println(-4 << 1);
        // right shift
        System.out.println(34 >> 1);
        System.out.println(-4 >> 1);
    }
}
